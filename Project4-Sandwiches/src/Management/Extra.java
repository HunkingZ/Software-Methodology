package Management;

/**
 *
 * @author Hanqing Zhao, Richard Xu
 */
public class Extra {
    String name;

    /**
     *
     * @param name
     */
    public Extra(String name) {
        this.name = name;
    }

    /**
     *
     * @return
     */
    @Override public String toString() {
        return name;
    }
}
